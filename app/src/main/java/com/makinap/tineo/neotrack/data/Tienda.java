public class Tienda {

    private int id;
    private String name;
    private int state;

    public int getIdTienda() {
        return id;
    }

    public void setIdTienda(int idTienda) {
        this.id = idTienda;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }
}
